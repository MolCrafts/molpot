CONFIG_PATH = Path.home() / ".molcrafts.config"
APP_PATH = Path.home() / ".molpot_app"

def load_config():
    if CONFIG_PATH.exists():
        with open(CONFIG_PATH, "r") as f:
            return json.load(f)
        
def save_config(config: dict):
    with open(CONFIG_PATH, "w") as f:
        json.dump(config, f)

@config_cli.command(name="set")
def config_set(key: str, value: str):
    config = load_config() or {}
    config[key] = value
    save_config(config)
    typer.echo(f"Set {key} to {value}")

@config_cli.command(name="get")
def config_get(key: str):
    config = load_config()
    if config is None:
        typer.echo("No config found")
    else:
        typer.echo(config.get(key))

def reset_app(dest_path: Path|None = None):
    if dest_path is None:
        dest_path = Path.home() / ".molpot_app"
        if not dest_path.exists():
            dest_path.mkdir()
            with open(dest_path / "__init__.py", "w") as f:
                f.write(f"# This file is generated by molpot")
        if not (dest_path / "app.json").exists():
            with open(dest_path / "app.json", "w") as f:
                f.write("{}")
    return dest_path


def preload_app():

    app_dir = Path.home() / ".molpot_app"
    if not app_dir.exists() and not (app_dir/"app.json").exists():
        return
    all_apps = {}
    for app_script in app_dir.iterdir():
        if app_script.suffix != ".py" or app_script.name == "__init__.py":
            continue
        apps = inspect_app_script(app_script)
        all_apps.update(apps)
    assert len(set([app.name for app in all_apps.values()])) == len(all_apps), "App name should be unique"
    # print(set([app.name for app in all_apps.values()]))
    for app in all_apps.values():
        
        @click.group()
        @click.argument('a')
        @click.pass_context
        def init_app(ctx, a):
            print("Init app")
            ctx.obj = app(a)

        pass_init_app = click.make_pass_decorator(app)
        init_app.command()(
            pass_init_app(click.argument("b")(app.run))
        )


        typer_click_object.add_command(init_app, name=app.name)
    return all_apps


