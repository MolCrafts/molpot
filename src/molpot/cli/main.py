@app_cli.command()
def install(script_path: Path, dest_path: Path|None = None, editable: Annotated[bool, typer.Option("--editable", "-e")] = False):
    """Install a script to the molpot app directory"""
    dest_path = reset_app(dest_path)
    if editable:
        # print(script_path, dest_path / script_path.name)
        # script_path.absolute().symlink_to(dest_path / script_path.name)
        (dest_path / script_path.name).symlink_to(script_path.absolute())
    else:
        # copy
        import shutil
        shutil.copy(script_path, dest_path / script_path.name)

    app = inspect_app_script(script_path)
    import json
    with open(dest_path / "app.json", "r") as f:
        app_meta = json.load(f)

    for class_name, app in app.items():
        app_meta[f"{script_path}::{class_name}"] = {
            "name": app.name,
            "version": app.version,
        }

    with open(dest_path / "app.json", "w") as f:
        json.dump(app_meta, f)

